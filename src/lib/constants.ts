// Helper to create set data with box pricing
function makeSet(
  id: string, name: string, slug: string, code: string, description: string,
  boxCount: number, pricePerBox: number, stock: number, status: "in-stock" | "pre-order" | "sold-out",
  releaseDate: string, packsPerBox: number, cardsPerPack: number,
  islandName: string, islandColor: string, islandGradient: string,
  islandDescription: string, islandArc: string, islandKeyCharacters: string[],
  category?: "booster" | "extra" | "premium" | "starter",
): TCGSet {
  return {
    id, name, slug, code, description,
    image: `/images/sets/${id}.png`,
    boxCount, pricePerBox, stock, status, releaseDate,
    packsPerBox, cardsPerPack,
    category,
    islandTheme: {
      name: islandName,
      color: islandColor,
      gradient: islandGradient,
      description: islandDescription,
      arc: islandArc,
      keyCharacters: islandKeyCharacters,
    },
  };
}

