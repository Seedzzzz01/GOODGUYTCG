export const getRankBySpent = (totalSpent: number): BountyRank => {
  const sorted = [...BOUNTY_RANKS].sort((a, b) => b.minSpent - a.minSpent);
  return sorted.find((r) => totalSpent >= r.minSpent) || BOUNTY_RANKS[0];
};

export const formatPrice = (price: number): string => {
  return new Intl.NumberFormat("th-TH").format(price);
};
